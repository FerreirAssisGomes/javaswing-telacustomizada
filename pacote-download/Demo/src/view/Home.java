    private void selecionar_Separador(Component cc) {
        Component[] cm = new Component[]{jPanel14, jPanel15, jPanel16};
        int i = 2;
        for (Component c : cm) {
            if (cc.equals(c)) {
                c.setBackground(new ColorUIResource(240, 240, 240));
                c.setName("S");
                c.getComponentAt(0, 0).setForeground(new ColorUIResource(41, 83, 145));
                ((CardLayout) jPanel33.getLayout()).show(jPanel33, "card" + i);
            } else {
                c.setBackground(new ColorUIResource(41, 83, 145));
                c.setName(null);
                c.getComponentAt(0, 0).setForeground(new ColorUIResource(255, 255, 255));
            }
            i++;
        }
    }

