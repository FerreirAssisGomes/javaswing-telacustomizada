    boolean max = false;
    private void jLabel22MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel22MouseClicked
        dimensao_atual = this.getSize();
        if ((GraphicsEnvironment.getLocalGraphicsEnvironment().getMaximumWindowBounds().getSize().width != dimensao_atual.width
                && GraphicsEnvironment.getLocalGraphicsEnvironment().getMaximumWindowBounds().getSize().height != dimensao_atual.height)
                && !max) {
            ponto_restauro = this.getLocation();
            max = true;
            dimensao_aux = dimensao_atual;
            this.setBounds(GraphicsEnvironment.getLocalGraphicsEnvironment().getMaximumWindowBounds());
            jLabel28.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/re.png")));
            jLabel22.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/reB.png")));
        } else {
            this.setSize(dimensao_aux);
            this.setLocation(ponto_restauro);
            max = false;
            jLabel28.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/maP.png")));
            jLabel22.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/ma.png")));
        }
    }//GEN-LAST:event_jLabel22MouseClicked

